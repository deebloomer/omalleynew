class ProjectsController < ApplicationController

  before_filter :logged_in_required, except: [:index, :show] #bounce to homepage REMOVE THIS IF DB WANTS TO ADD PROJ
  rescue_from ActiveRecord::RecordNotFound, with: :invalid_project
  before_filter :get_variables

  def index
    @projects = Project.all
    @project = @projects.first
    respond_to do |format|
      format.html # index.html.erb
      format.json { render json: @projects }
    end
  end

  # GET /projects/1
  # GET /projects/1.json
  def show
    @project = Project.find(params[:id])
    respond_to do |format|
      format.html # show.html.erb
      format.json { render json: @project }
    end
  end

  # GET /projects/new
  # GET /projects/new.json
  def new
    @project = Project.new
    @project.photos.build
    respond_to do |format|
      format.html # new.html.erb
      format.json { render json: @project }
    end
  end

  # GET /projects/1/edit
  def edit
    @project = Project.find(params[:id])
  end

  # POST /projects
  # POST /projects.json
  def create
    @project = Project.new(project_params)
    respond_to do |format|
      if @project.save
        format.html { redirect_to @project, notice: 'Project was successfully created.' }
        format.json { render json: @project, status: :created, location: @project }
      else
        format.html { render action: "new" }
        format.json { render json: @project.errors, status: :unprocessable_entity }
      end
    end
  end

  # PUT /projects/1
  # PUT /projects/1.json
  def update
    @project = Project.find(params[:id])

    respond_to do |format|
      if @project.update_attributes(project_params)
        format.html { redirect_to @project, notice: 'Project was successfully updated.' }
        format.json { head :no_content }
      else
        format.html { render action: "edit" }
        format.json { render json: @project.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /projects/1
  # DELETE /projects/1.json
  def destroy
    @project = Project.find(params[:id])
    @project.destroy

    respond_to do |format|
      format.html { redirect_to projects_url }
      format.json { head :no_content }
    end
  end

  protected

  def project_params
    params.require(:project).permit(:description, :design_team, :name, :project_type_id, :running_order)
  end

  private
  def get_variables
    @project_types = ProjectType.all
    @projects = Project.all
  end

  def invalid_project
    logger.error "attempt to access invalid project #{params[:id]}"
    flash[:notice] = "Sorry, I couldn't find that!"                # this does not seem to work
    redirect_to projects_url
  end

end

